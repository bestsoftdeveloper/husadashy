            Clubul Sportiv Hayashi Activ a fost infiintat in anul 2002 de catre antrenorul Adrian Gherman.
            In anul 2002 a fost afiliat la Federatia Romana de Karate W.K.C., stilul practicat fiind shotokan.
            Astazi, clubul nostru sportiv se evidentiaza in peisajul vietii sportive a localitatii Floresti, judetul Cluj,
            prin premiile obtinute de sportivii nostri. Performantele obtinute de acestia la diverse competitii sportive
            trebuie subliniate si mai mult, daca e sa tinem cont de aria de selectie relativ restransa in care clubul isi defasoara activitatea.
            <br/><br/>
            Clubul Hayashi Activ, sectia karate ofera program de initiere pentru copii incepand de la varsta prescolara. Pentru mai
            multe informatii, nu ezitati sa ne contactati.
            <br/><br/>
            Incepand cu anul 2009, clubul isi diversifica activitatile sportive prin infintarea a doua noi sectii, sectia de fotbal si sectia de baschet.
